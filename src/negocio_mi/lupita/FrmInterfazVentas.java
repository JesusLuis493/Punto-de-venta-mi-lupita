    public void mostrarSubtotal()
{
    Conexion_BD conect = new Conexion_BD();
    Connection con = conect.conectar();
    PreparedStatement ps1 = null;  // ← Nueva variable
    PreparedStatement ps2 = null;  // ← Nueva variable
    ResultSet rs1 = null;
    ResultSet rs2 = null;

    try {
        // 1️⃣ Obtener el último ID de venta
        String sqlMaxId = "SELECT MAX(id_Ventas) as id_Ventas FROM Ventas";
        ps1 = con.prepareStatement(sqlMaxId);
        rs1 = ps1.executeQuery();
        
        int idVenta = 0;
        if (rs1.next()) {
            idVenta = rs1.getInt("id_Ventas");
        }
        
        if (idVenta == 0) {
            TFSubtotal.setText("0.00");
            return;
        }
        
        // 2️⃣ Obtener el subtotal
        String sql = "SELECT SUM(Subtotal) as Subtotal FROM Detalles_venta WHERE id_Ventas = ?";
        ps2 = con.prepareStatement(sql);
        ps2.setInt(1, idVenta);  // ← Ahora es ps2
        rs2 = ps2.executeQuery();
        
        if (rs2.next()) {
            double subtotal = rs2.getDouble("Subtotal");
            TFSubtotal.setText(String.format("%.2f", subtotal));
            System.out.println("Subtotal: " + subtotal);
        }
        
        rs1.close();
        rs2.close();
        ps1.close();
        ps2.close();
        con.close();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        System.out.println("Error: " + e.getMessage());
        e.printStackTrace();
    }
}
    
    public void mostrarTotal()
    {
        Conexion_BD conect = new Conexion_BD();
        Connection con = conect.conectar();
    
    try {
        // Primero obtener el último id_venta registrado
        String sqlMaxId = "SELECT MAX(id_Ventas) as id_Ventas FROM Ventas";
        ps = con.prepareStatement(sqlMaxId);
        rs = ps.executeQuery();
        
        int idVenta = 0;
        if (rs.next()) {
            idVenta = rs.getInt("id_Ventas");
        }
        
        // Luego calcular el total con ese id_venta
        String sql = "SELECT SUM(dv.Subtotal * (1 - v.Descuento/100)) as total FROM Detalles_venta dv INNER JOIN Ventas v ON dv.id_Ventas = v.id_Ventas WHERE dv.id_Ventas = ?";
        ps = con.prepareStatement(sql);
        ps.setInt(1, idVenta);
        rs = ps.executeQuery();
        
        if (rs.next()) {
            double total = rs.getDouble("total");
            TFTotal.setText(String.format("%.2f", total));
        }
        
        
        rs.close();
        ps.close();
        con.close();
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
    }
    }
